function NavItem({
  item,
  isCollapsed,
  pathname,
  expandedSection,
  onToggleSection,
}: {
  item: NavigationItem;
  isCollapsed: boolean;
  pathname: string;
  expandedSection: string | null;
  onToggleSection: (name: string) => void;
}) {
  const isExpanded = expandedSection === item.name;

  const activeSubsection = item.subsections?.reduce<NavigationItem | undefined>(
    (best, sub) => {
      if (!sub.href) return best;
      const isMatch =
        pathname === sub.href || pathname.startsWith(`${sub.href}/`);
      if (!isMatch) return best;
      if (!best || (best.href && sub.href.length > best.href.length)) {
        return sub;
      }
      return best;
    },
    undefined,
  );

  // Check if this item or any of its sub-items match the current path
  const isActive = item.href ? pathname === item.href : false;
  const hasActiveChild = !!activeSubsection;

  // If item has subsections
  if (item.subsections && item.subsections.length > 0) {
    return (
      <div>
        <button
          onClick={() => !isCollapsed && onToggleSection(item.name)}
          className={`w-full flex items-center ${isCollapsed ? "justify-center" : "justify-between"} px-3 py-2.5 text-sm font-medium rounded-lg transition-colors ${
            hasActiveChild
              ? "bg-blue-50 text-blue-700"
              : "text-gray-600 hover:bg-gray-50 hover:text-gray-900"
          }`}
          title={isCollapsed ? item.name : undefined}
        >
          <div className="flex items-center">
            <span
              className={`${isCollapsed ? "" : "mr-3"} ${
                hasActiveChild ? "text-blue-600" : "text-gray-400"
              }`}
            >
              <Icon name={item.icon} />
            </span>
            {!isCollapsed && <span>{item.name}</span>}
          </div>
          {!isCollapsed && (
            <svg
              className={`w-4 h-4 text-gray-400 transition-transform duration-200 ${
                isExpanded ? "rotate-180" : ""
              }`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M19 9l-7 7-7-7"
              />
            </svg>
          )}
        </button>
        {!isCollapsed && isExpanded && (
          <div className="mt-1 ml-2 pl-4 border-l-2 border-gray-100 space-y-0.5">
            {item.subsections.map((subItem) => {
              const isSubActive =
                !!subItem.href && activeSubsection?.href === subItem.href;
              return (
                <Link
                  key={subItem.name}
                  href={subItem.href || "#"}
                  className={`flex items-center px-3 py-2 text-sm rounded-lg transition-colors ${
                    isSubActive
                      ? "bg-blue-50 text-blue-700 font-medium"
                      : "text-gray-500 hover:bg-gray-50 hover:text-gray-900"
                  }`}
                >
                  <span
                    className={`mr-3 ${
                      isSubActive ? "text-blue-500" : "text-gray-400"
                    }`}
                  >
                    <Icon name={subItem.icon} className="w-4 h-4" />
                  </span>
                  {subItem.name}
                </Link>
              );
            })}
          </div>
        )}
      </div>
    );
  }

  // Regular menu item (no subsections)
  return (
    <Link
      href={item.href || "#"}
      className={`flex items-center ${isCollapsed ? "justify-center" : ""} px-3 py-2.5 text-sm font-medium rounded-lg transition-colors ${
        isActive
          ? "bg-blue-50 text-blue-700"
          : "text-gray-600 hover:bg-gray-50 hover:text-gray-900"
      }`}
      title={isCollapsed ? item.name : undefined}
    >
      <span
        className={`${isCollapsed ? "" : "mr-3"} ${
          isActive ? "text-blue-600" : "text-gray-400"
        }`}
      >
        <Icon name={item.icon} />
      </span>
      {!isCollapsed && item.name}
    </Link>
  );
}

function Sidebar({
  user,
  navigation,
  isOpen,
  onToggle,
  onLogout,
}: SidebarProps) {
  const pathname = usePathname();
  const [expandedSection, setExpandedSection] = useState<string | null>(null);

  // Auto-expand the section whose sub-item matches the current URL
  useEffect(() => {
    for (const item of navigation) {
      if (item.subsections) {
        const hasMatch = item.subsections.some((sub) =>
          sub.href ? pathname.startsWith(sub.href) : false,
        );
        if (hasMatch) {
          setExpandedSection(item.name);
          return;
        }
      }
    }
  }, [pathname, navigation]);

  const handleToggleSection = (name: string) => {
    setExpandedSection((prev) => (prev === name ? null : name));
  };

  return (
    <aside
      className={`fixed inset-y-0 left-0 z-50 bg-white shadow-lg transition-all duration-300 ease-in-out ${
        isOpen ? "w-64" : "w-16"
      }`}
    >
      <div className="flex flex-col h-full">
        {/* User Info with Toggle Button - synced with header height (h-[72px] = py-4 + content) */}
        {isOpen ? (
          <div className="h-[72px] px-4 border-b shadow-sm flex items-center">
            <div className="flex items-center justify-between w-full">
              <div className="flex items-center space-x-3 flex-1 min-w-0">
                <UserAvatar name={user.full_name} size="md" />
                <div className="flex-1 min-w-0">
                  <p className="text-sm font-medium text-gray-900 truncate">
                    {user.full_name}
                  </p>
                  <p className="text-xs text-gray-500 truncate">
                    {user.role.name}
                  </p>
                </div>
              </div>
              <button
                onClick={onToggle}
                className="p-2 text-gray-500 hover:text-gray-700 hover:bg-gray-100 rounded-lg transition-colors ml-2"
                aria-label="Collapse sidebar"
              >
                <svg
                  className="w-5 h-5"
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M11 19l-7-7 7-7m8 14l-7-7 7-7"
                  />
                </svg>
              </button>
            </div>
          </div>
        ) : (
          <div className="h-[72px] px-2 border-b shadow-sm flex items-center justify-center">
            <button
              onClick={onToggle}
              className="p-2 text-gray-500 hover:text-gray-700 hover:bg-gray-100 rounded-lg transition-colors"
              aria-label="Expand sidebar"
            >
              <svg
                className="w-5 h-5"
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M13 5l7 7-7 7M5 5l7 7-7 7"
                />
              </svg>
            </button>
          </div>
        )}

        {/* Navigation */}
        <nav className="flex-1 p-2 space-y-0.5 overflow-y-auto">
          {navigation.map((item) => (
            <NavItem
              key={item.name}
              item={item}
              isCollapsed={!isOpen}
              pathname={pathname}
              expandedSection={expandedSection}
              onToggleSection={handleToggleSection}
            />
          ))}
        </nav>

        {/* Logout Button */}
        <div className="p-4 border-t">
          <button
            onClick={onLogout}
            className={`w-full flex items-center ${isOpen ? "justify-center" : "justify-center"} px-4 py-3 text-sm font-medium text-white bg-red-600 rounded-lg hover:bg-red-700 transition-colors`}
            title={!isOpen ? "Logout" : undefined}
          >
            <svg
              className={`w-5 h-5 ${isOpen ? "mr-2" : ""}`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1"
              />
            </svg>
            {isOpen && "Logout"}
          </button>
        </div>
      </div>
    </aside>
  );
}

export default React.memo(Sidebar);
